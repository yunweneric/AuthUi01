class AppScene {
  final String title;
  final String desc;
  final String longDesc;
  final int index;
  final String videoUrl;
  final String thumbnail;
  final String duration;

  AppScene({
    required this.title,
    required this.longDesc,
    required this.desc,
    required this.index,
    required this.videoUrl,
    required this.thumbnail,
    required this.duration,
  });
}
